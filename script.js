// Add interactive cursor effect (optional enhancement)
document.addEventListener('mousemove', (e) => {
    const interactiveElements = document.querySelectorAll('.btn, .nav-link, .social-link, .project-card');
    
    interactiveElements.forEach(element => {
        const rect = element.getBoundingClientRect();
        const x = e.clientX - rect.left;
        const y = e.clientY - rect.top;
        
        if (x >= 0 && x <= rect.width && y >= 0 && y <= rect.height) {
            element.style.transform = `perspective(1000px) rotateX(${(y - rect.height / 2) / 10}deg) rotateY(${(x - rect.width / 2) / -10}deg)`;
        } else {
            element.style.transform = '';
        }
    });
});

